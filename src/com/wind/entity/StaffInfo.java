package com.wind.entity;

import java.util.Date;

/**
 * @Description 员工信息表
 *
 * @Author 唐耀家
 *
 * @Date 2018 年9月6日 下午 14：56：23
 */
public class StaffInfo {
    private String staffNo; //工号
    private String staffPassWord; //密码
    private long agentId; 		//所属网点编号
    private String staffName; //姓名
    private int staffGender; //性别
    private int staffAge; //年龄
    private int categoryId; //类型编号
    private String staffTel; //电话
    private Date creationDate;//表创建时间
    private Date modifyDate;//表修改时间
    private String rightName;//员工角色
    
    
    
    public String getRightName() {
		return rightName;
	}

	public void setRightName(String rightName) {
		this.rightName = rightName;
	}

	public Date getCreationDate() {
		return creationDate;
	}

	public void setCreationDate(Date creationDate) {
		this.creationDate = creationDate;
	}

	public Date getModifyDate() {
		return modifyDate;
	}

	public void setModifyDate(Date modifyDate) {
		this.modifyDate = modifyDate;
	}
	
	public long getAgentId() {
		return agentId;
	}

	public void setAgentId(long agentId) {
		this.agentId = agentId;
	}

	public String getStaffPassWord() {
		return staffPassWord;
	}

	public void setStaffPassWord(String staffPassWord) {
		this.staffPassWord = staffPassWord;
	}

    public String getStaffNo() {
		return staffNo;
	}

	public void setStaffNo(String staffNo) {
		this.staffNo = staffNo;
	}

	public String getStaffName() {
        return staffName;
    }

    public void setStaffName(String staffName) {
        this.staffName = staffName;
    }

    public int getStaffGender() {
        return staffGender;
    }

    public void setStaffGender(int staffGender) {
        this.staffGender = staffGender;
    }

    public int getStaffAge() {
        return staffAge;
    }

    public void setStaffAge(int staffAge) {
        this.staffAge = staffAge;
    }

    public int getCategoryId() {
        return categoryId;
    }

    public void setCategoryId(int categoryId) {
        this.categoryId = categoryId;
    }

    public String getStaffTel() {
        return staffTel;
    }

    public void setStaffTel(String staffTel) {
        this.staffTel = staffTel;
    }
}
