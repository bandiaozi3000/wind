public class CarFlowPath {
	private int carId;//车辆id
	private String carNum;//车牌号
	private long carNowAgent;//车辆现在所在网点编号
	private long carArriveAgent;//车辆所要到达的网点编号
	private String carNowAgentName;//车辆现在所在网点名字
	private String carArriveAgentName;//车辆所要到达的网点名字
	
	
	
	public int getCarId() {
		return carId;
	}
	public void setCarId(int carId) {
		this.carId = carId;
	}
	public String getCarNum() {
		return carNum;
	}
	public void setCarNum(String carNum) {
		this.carNum = carNum;
	}
	public long getCarNowAgent() {
		return carNowAgent;
	}
	public void setCarNowAgent(long carNowAgent) {
		this.carNowAgent = carNowAgent;
	}
	public long getCarArriveAgent() {
		return carArriveAgent;
	}
	public void setCarArriveAgent(long carArriveAgent) {
		this.carArriveAgent = carArriveAgent;
	}
	public String getCarNowAgentName() {
		return carNowAgentName;
	}
	public void setCarNowAgentName(String carNowAgentName) {
		this.carNowAgentName = carNowAgentName;
	}
	public String getCarArriveAgentName() {
		return carArriveAgentName;
	}
	public void setCarArriveAgentName(String carArriveAgentName) {
		this.carArriveAgentName = carArriveAgentName;
	}
	
}
