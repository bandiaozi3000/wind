package com.wind.entity.combine;

import java.util.Date;

/**
 * 异常信息联查类
 * @author 胡威
 * @date 2018年9月15日 上午8:12:17
 */
public class AnomalyInfo {
	private String orderNo;//订单号
	private String statusName;//状态名称
	private String issueInfo;//问题详情
	private String agentName;//网点名
	private Date creationDate;//录入时间
	private Date modifyDate;//处理事件
	private String handling;//处理详情
	private int statusCode;//状态码 
	private int handledCode;//处理状态码
	private int agentNumber;//网点号
	public String getOrderNo() {
		return orderNo;
	}
	public void setOrderNo(String orderNo) {
		this.orderNo = orderNo;
	}
	public String getStatusName() {
		return statusName;
	}
	public void setStatusName(String statusName) {
		this.statusName = statusName;
	}
	public String getIssueInfo() {
		return issueInfo;
	}
	public void setIssueInfo(String issueInfo) {
		this.issueInfo = issueInfo;
	}
	public String getAgentName() {
		return agentName;
	}
	public void setAgentName(String agentName) {
		this.agentName = agentName;
	}
	public Date getCreationDate() {
		return creationDate;
	}
	public void setCreationDate(Date creationDate) {
		this.creationDate = creationDate;
	}
	public Date getModifyDate() {
		return modifyDate;
	}
	public void setModifyDate(Date modifyDate) {
		this.modifyDate = modifyDate;
	}
	public String getHandling() {
		return handling;
	}
	public void setHandling(String handling) {
		this.handling = handling;
	}
	public int getStatusCode() {
		return statusCode;
	}
	public void setStatusCode(int statusCode) {
		this.statusCode = statusCode;
	}
	public int getHandledCode() {
		return handledCode;
	}
	public void setHandledCode(int handledCode) {
		this.handledCode = handledCode;
	}
	public int getAgentNumber() {
		return agentNumber;
	}
	public void setAgentNumber(int agentNumber) {
		this.agentNumber = agentNumber;
	}
	
}
